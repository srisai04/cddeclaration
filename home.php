<p><strong>Important notes – READ these first</strong></p>
<p>  Completing  your CD Declaration will take between 20-45mins, depending on your involvement  with CDs.  There are a number of  different sections; at each section you will be asked if it is relevant to you  (for example &lsquo;Do you prescribe CDs?&rsquo;.  If  you answer yes, you will then be asked a range of further questions.  If you answer no, you will be taken to the  next section.   You can stop and start  the self-declaration when you want, and it will save your answers from previous  completed sections.  Once you have  completed a section and moved to the next, you cannot go back and amend your  answers.</p>
<p>&nbsp;</p>
<p><strong>Need further help?</strong></p>
<p>  If you have  any further questions, or want to discuss your CD self-declaration in more  detail, then please contact your local PCT/Cluster Accountable Officer.  You should have received their contact  details in the email you received which registered you with this site. </p>
<p>&nbsp; </p>
<p><strong>Regulations for CD declarations</strong></p>
<p>  The  regulations detail the organisational responsibilities for designated bodies and their Accountable  Officers, and include requesting a declaration of assurance of  appropriate CD management for the prescribing, possession and administration of  CDs is made by1:</p>
<ul>
  <li>ALL  general medical and dental practitioners on the PCT&rsquo;s performers lists. This is  covered by Regulation 12 of <em>The Controlled Drugs (Supervision of</em> <em>Management  and Use) Regulations 20061</em></li>
  <li>Other  service providers operating within the area (other than those inspected by GPhC  or CQC). Although not covered within the regulations, the National Prescribing  Centre Handbook states this <em>&lsquo;would be  considered good practice for the AO to request a declaration of assurance from  these providers&rsquo;2</em></li>
</ul>
